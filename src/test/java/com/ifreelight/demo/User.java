package com.ifreelight.demo;

import com.github.yuebo.dyna.service.jdbc.Query;
import com.github.yuebo.dyna.service.jdbc.Table;

/**
 * User: yuebo
 * Date: 2018/7/5
 * Time: 22:04
 */
@Table("tbl_user")
public class User {
    private String id;
    @Query
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
